def calculate_damage(attack, defense, level):
    """
    Рассчитывает итоговое значение урона персонажа с учетом защиты цели и бонуса уровня.

    Args:
        attack (int | float): Базовое значение силы атаки атакующего персонажа.
        defense (int | float): Показатель защиты цели, снижающий входящий урон.
        level (int): Текущий уровень атакующего, дающий дополнительный бонус к урону.

    Returns:
        int: Итоговое целое число нанесенного урона (минимум 1).

    Example:
        >>> calculate_damage(10, 5, 2)
        17
    """
    base = attack * 1.5
    reduction = defense * 0.3
    bonus = level * 2
    final = base - reduction + bonus
    
    if final < 1:
        final = 1
        
    return int(final)

